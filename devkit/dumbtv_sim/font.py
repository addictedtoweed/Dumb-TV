def _pixels(ch, fg=1):
    """A GH*GW list of indices (fg where the 5x7 bit is set, 0 elsewhere)."""
    rows = _F.get(ch.upper(), _F[" "])
    px = [0] * (GW * GH)
    for r in range(7):
        for c in range(5):
            if (rows[r] >> (4 - c)) & 1:
                px[r * GW + c] = fg
    return px


def upload(send, fg=1):
    """Upload the whole font via GLYPH_UPLOAD (slot = character code)."""
    for ch in _F:
        send(P.OP_GUP, bytes([ord(ch)]) + bytes(_pixels(ch, fg)))
